########## Arithmetic operators ##########

print("########## Arithmetic operators ##########")
num1=2
num2=3
# Addition
print("num1+num2=",num1+num2)
# Substract
print("num1-num2=",num1-num2)
# Division
print("num1/num2=",num1/num2)
# Multiplication
print("num1*num2=",num1*num2)
# Modulus (Gives you the reminder)
print("num1%num2=",num1%num2)
# Exponential
print("2^3=",num1**num2)
# Float (Rounding off the decimal part)
print("22//7=",22//7)

